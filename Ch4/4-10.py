players = ['charles', 'martina', 'michael', 'florence', 'eli']
print(players[0:3]) #從第0號開始，一直到第3號以前(不含第3號)，把元素印出來。
# ---------------------------------------------------------------------------------------------------------------------
print("The first three items in the list are:")
print(players[:3]) #前3個，不包含第4個(不包含第3號)
print(players[0:3])
print(players[0:-2])
print(players[:-2])
# ---------------------------------------------------------------------------------------------------------------------
print("Three items from the middle of the list are:")
print(players[1:-1])
print(players[-4:-1])
print(players[1:4])
print(players[-4:4])
# ---------------------------------------------------------------------------------------------------------------------
print("The last three items in the list are:")
print(players[-3:]) #從倒數第3個開始，一直到最後一個
print(players[2:]) #從2號(第3個)開始，一直到最後一個
print(players[2:5]) #從2號開始，一直到5號以前(最後一個是4號)
print(players[-3:5])



